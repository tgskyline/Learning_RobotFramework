public class JavaLoops_Tabuada {
    public static void main(String[] args) {
              
      for (int i = 1; i < 11; i++)
      {
        System.out.println("Tabuada do " + i);
      
        for (int j = 1; j < 11; j++)    
      
            System.out.println( i + " x " + j + " = " + (i *j));
      }    
    }
  }

